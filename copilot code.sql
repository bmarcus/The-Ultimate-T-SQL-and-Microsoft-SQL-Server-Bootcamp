-- Select work orders, the product, and their scrap reason - include records that have no scrap reason
SELECT 
    wo.WorkOrderID,
    p.ProductID,
    p.Name AS ProductName,
    wo.OrderQty,
    wo.ScrappedQty,
    wo.StartDate,
    wo.EndDate,
    wo.DueDate,
    sr.ScrapReasonID,
    sr.Name AS ScrapReasonName
FROM 
    Production.WorkOrder AS wo
LEFT JOIN 
    Production.Product AS p 
    ON wo.ProductID = p.ProductID
LEFT JOIN 
    Production.ScrapReason AS sr 
    ON wo.ScrapReasonID = sr.ScrapReasonID
WHERE 
    wo.ScrappedQty > 0 -- Only include work orders that have scrapped quantities
ORDER BY 
    wo.WorkOrderID,
    p.ProductID,
    sr.ScrapReasonID;

-- Select all customers and employees into a single result set. 
SELECT 
    c.CustomerID AS ID,
    'Customer' AS Type,
    c.AccountNumber AS Identifier,
    c.ModifiedDate
FROM 
    Sales.Customer AS c
UNION ALL
SELECT 
    e.BusinessEntityID AS ID,
    'Employee' AS Type,
    e.LoginID AS Identifier,
    e.ModifiedDate
FROM 
    HumanResources.Employee AS e;

-- Eliminate all duplicated names from the customer table
SELECT DISTINCT 
    c.FirstName,
    c.LastName
FROM    
    Person.Person AS c
WHERE 
    c.FirstName IS NOT NULL AND c.LastName IS NOT NULL  
    









